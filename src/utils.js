import io from 'socket.io-client';

export const downloadCloudFromUrl = async (url, fileName) => {
	const blob = await fetch(url)
		.then((res) => res.blob())
		.catch((err) => console.log("Couldn't download RapCloud", err));
	const downloadableUrl = URL.createObjectURL(blob);
	const a = document.createElement('a');
	a.href = downloadableUrl;
	a.download = fileName;
	document.body.appendChild(a);
	const clickHandler = () => {
		setTimeout(() => {
			URL.revokeObjectURL(downloadableUrl);
			a.removeEventListener('click', clickHandler);
			a.parentNode.removeChild(a);
		}, 150);
	};
	a.addEventListener('click', clickHandler, false);
	a.click();
};

export const classNames = (...classes) => {
	return classes.filter((c) => c).join(' ');
};

export const getRandomInt = (max) => {
	return Math.floor(Math.random() * Math.floor(max)) + 1;
};

export const getConnectedSocket = () => {
	return new Promise((resolve, reject) => {
		const API_URL =
			process.env.NODE_ENV === 'development'
				? 'http://localhost:3333'
				: 'https://rap-clouds-server.herokuapp.com';
		const socket = io(API_URL);
		socket.on('connect', () => {
			resolve(socket);
		});
	});
};
export const gcd = (a, b) => {
	if (!b) {
		return a;
	}
	return gcd(b, a % b);
};

/**
 * Display a base64 URL inside an iframe in another window.
 */
export const imageInNewTab = (url) => {
	const win = window.open();
	win.document.write(
		'<iframe src="' +
			url +
			'" frameborder="0" style="border:0; top:0px; left:0px; bottom:0px; right:0px; width:100%; height:100%;" allowfullscreen></iframe>',
	);
};

